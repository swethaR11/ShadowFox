#Store the principal amount, rate of interest, and time in different variables and then calculate the Simple Interest for 3 years. Formula: Simple Interest = P x R x T / 100
# Store values in variables
principal = 20000  # Principal amount in currency units
rate = 8         # Rate of interest in percent per annum
time = 2         # Time in years

# Calculate Simple Interest
simple_interest = (principal * rate * time) / 100

# Display the result
print("Simple Interest for 2 years is:", simple_interest)
